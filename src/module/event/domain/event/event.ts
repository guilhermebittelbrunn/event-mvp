import { isEmpty } from 'class-validator';

import EventSlug from './eventSlug';
import EventStatus from './eventStatus';

import EventAccess from '../eventAccess/eventAccess';
import { EventAccesses } from '../eventAccess/eventAccesses';
import EventConfig from '../eventConfig';
import { Memories } from '../memory/memories';

import Payment from '@/module/billing/domain/payment/payment';
import File from '@/module/shared/domain/file/file';
import User from '@/module/user/domain/user/user';
import Entity from '@/shared/core/domain/Entity';
import UniqueEntityID from '@/shared/core/domain/UniqueEntityID';
import GenericErrors from '@/shared/core/logic/genericErrors';
import Guard from '@/shared/core/logic/guard';
import { PartialAutoGenerated } from '@/shared/types/common';
import { EventStatusEnum } from '@/shared/types/event/event';

export interface IEventProps extends PartialAutoGenerated {
  userId: UniqueEntityID;
  fileId?: UniqueEntityID;
  paymentId?: UniqueEntityID;
  slug: EventSlug;
  status?: EventStatus;
  name: string;
  description?: string;
  startAt: Date;
  endAt: Date;

  config?: EventConfig;
  accesses?: EventAccesses;
  file?: File;
  payment?: Payment;
  user?: User;
  memories?: Memories;

  guestAccess?: EventAccess | undefined;
  ownerAccess?: EventAccess | undefined;
}

export default class Event extends Entity<IEventProps> {
  constructor(props: IEventProps, id?: UniqueEntityID) {
    super(props, id);
  }

  get id(): UniqueEntityID {
    return this._id;
  }

  get userId(): UniqueEntityID {
    return this.props.userId;
  }

  get fileId(): UniqueEntityID | undefined {
    return this.props.fileId;
  }

  set fileId(fileId: UniqueEntityID | undefined) {
    this.props.fileId = fileId;
  }

  get paymentId(): UniqueEntityID | undefined {
    return this.props.paymentId;
  }

  set paymentId(paymentId: UniqueEntityID | undefined) {
    this.props.paymentId = paymentId;
  }

  get slug(): EventSlug {
    return this.props.slug;
  }

  get status(): EventStatus {
    return this.props.status;
  }

  set status(status: EventStatus) {
    this.props.status = status;
  }

  get name(): string {
    return this.props.name;
  }

  get description(): string | undefined {
    return this.props.description;
  }

  get startAt(): Date {
    return this.props.startAt;
  }

  get endAt(): Date {
    return this.props.endAt;
  }

  get totalMemories(): number {
    return this.props.memories?.totalMemories || 0;
  }

  get createdAt(): Date {
    return this.props.createdAt;
  }

  get updatedAt(): Date {
    return this.props.updatedAt;
  }

  get deletedAt(): Date | undefined {
    return this.props.deletedAt;
  }

  get config(): EventConfig | undefined {
    return this.props.config;
  }

  set config(config: EventConfig) {
    this.props.config = config;
  }

  get memories(): Memories {
    if (isEmpty(this.props.memories)) {
      this.props.memories = Memories.create();
    }

    return this.props.memories;
  }

  set memories(memories: Memories) {
    this.props.memories = memories;
  }

  get accesses(): EventAccesses {
    if (isEmpty(this.props.accesses)) {
      this.props.accesses = EventAccesses.create();
    }

    return this.props.accesses;
  }

  set accesses(accesses: EventAccesses) {
    this.props.accesses = accesses;
  }

  get guestAccess(): EventAccess | undefined {
    return this.props.accesses?.guestAccess;
  }

  get ownerAccess(): EventAccess | undefined {
    return this.props.accesses?.ownerAccess;
  }

  get file(): File | undefined {
    return this.props.file;
  }

  set file(file: File) {
    this.props.file = file;
  }

  get user(): User | undefined {
    return this.props.user;
  }

  get payment(): Payment | undefined {
    return this.props.payment;
  }

  set payment(payment: Payment) {
    this.props.payment = payment;
  }

  public static create(props: IEventProps, id?: UniqueEntityID) {
    const guardedProps = Guard.againstNullOrUndefinedBulk([
      { argument: props.name, argumentName: 'nome' },
      { argument: props.userId, argumentName: 'id do usu√°rio' },
      { argument: props.slug, argumentName: 'link de acesso' },
      { argument: props.startAt, argumentName: 'data de inicio' },
      { argument: props.endAt, argumentName: 'data de fim' },
    ]);

    if (!guardedProps.succeeded) {
      throw new GenericErrors.InvalidParam(guardedProps.message);
    }

    const propsWithDefaults = {
      status: EventStatus.create(EventStatusEnum.DRAFT),
      ...props,
    };

    return new Event(propsWithDefaults, id);
  }
}
